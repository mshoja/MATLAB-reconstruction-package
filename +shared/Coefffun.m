function Coeff = Coefffun(J)
    %The binomial-like coefficient matrix Coeff
    if J > 20
        %for large J create functions
        syms w
        Coeff = cell(1, J);
        Coeff{1} = [];
        Coeff{2} = [];
        for j = 3:J
            A = zeros(1, j + 1);
            for k = 1:j + 1
                A(k) = (-1)^(k - 1) * nchoosek(j, k - 1);
            end
            C = A .* w.^(0:j);
            Coeff{j} = MyMatlabFunction1(C, w, w);
        end
    else
        %For small J use created functions
        Coeff = cell(1, J);
        Coeff{1} = [];
        Coeff{2} = [];
        if J < 3
            return;
        end
